void vga_write_registers(unsigned char* registers) {
    outb(VGA_MISC_WRITE, *registers);
    registers++;

    for(int i = 0; i < VGA_NUM_SEQ_REGS; i++) {
        outb(VGA_SEQ_INDEX, i);
        outb(VGA_SEQ_DATA, *registers);
        registers++;
    }

    outb(VGA_CRTC_INDEX, 0x03);
    outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) | 0x80);
    outb(VGA_CRTC_INDEX, 0x11);
    outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) & ~0x80);
    registers[0x03] |= 0x80;
    registers[0x11] &= ~0x80;

    for(int i = 0; i < VGA_NUM_CRTC_REGS; i++) {
        outb(VGA_CRTC_INDEX, i);
        outb(VGA_CRTC_DATA, *registers);
        registers++;
    }

    for(int i = 0; i < VGA_NUM_GC_REGS; i++) {
        outb(VGA_GC_INDEX, i);
        outb(VGA_GC_DATA, *registers);
        registers++;
    }

    for(int i = 0; i < VGA_NUM_AC_REGS; i++) {
        inb(VGA_INSTAT_READ);
        outb(VGA_AC_INDEX, i);
        outb(VGA_AC_WRITE, *registers);
        registers++;
    }

    inb(VGA_INSTAT_READ);
    outb(VGA_AC_INDEX, 0x20);
}

void vga_set_plane(unsigned int plane) {
    unsigned char pmask;

    plane &= 3;

    pmask = 1 << plane;
    outb(VGA_GC_INDEX, 4);
    outb(VGA_GC_DATA, plane);
    outb(VGA_SEQ_INDEX, 2);
    outb(VGA_SEQ_DATA, pmask);
}

void vga_write_font(unsigned char* font) {
    outb(VGA_SEQ_INDEX, 2);
    unsigned char seq2 = inb(VGA_SEQ_DATA);
    
    outb(VGA_SEQ_INDEX, 4);
    unsigned char seq4 = inb(VGA_SEQ_DATA);
    outb(VGA_SEQ_DATA, seq4 | 0x04);

    outb(VGA_GC_INDEX, 4);
    unsigned char gc4 = inb(VGA_GC_DATA);

    outb(VGA_GC_INDEX, 5);
    unsigned char gc5 = inb(VGA_GC_DATA);
    outb(VGA_GC_DATA, gc5 & ~0x10);

    outb(VGA_GC_INDEX, 6);
    unsigned char gc6 = inb(VGA_GC_DATA);
    outb(VGA_GC_DATA, gc6 & ~0x02);

    vga_set_plane(2);
    for(int i = 0; i < 256; i++) {
        __builtin_memcpy((void*)(0xB8000 + i * 32), font, 16);
        font += 16;
    }

    outb(VGA_SEQ_INDEX, 2);
    outb(VGA_SEQ_DATA, seq2);
    outb(VGA_SEQ_INDEX, 4);
    outb(VGA_SEQ_DATA, seq4);
    outb(VGA_GC_INDEX, 4);
    outb(VGA_GC_DATA, gc4);
    outb(VGA_GC_INDEX, 5);
    outb(VGA_GC_DATA, gc5);
    outb(VGA_GC_INDEX, 6);
    outb(VGA_GC_DATA, gc6);
}

void vga_set_colormap(char colormap[256][3]) {
    outb(0x3C6, 0xff);

    for(int i = 0; i < 256; i++) {
        outb(0x3C8, i);
        outb(0x3C9, (colormap[i][0] >> 2) & 0x3F);
        outb(0x3C9, (colormap[i][1] >> 2) & 0x3F);
        outb(0x3C9, (colormap[i][2] >> 2) & 0x3F);
    }
}

void module_init_vga_bootscreen() {
    vga_write_registers(mode_graphics_hicolor);
    vga_set_colormap(header_data_cmap);

    char* vga_memory = (char*)0xA0000;
    
    for(int y = 0; y < height; y++) {
        for(int x = 0; x < width; x++) {
            char pixel = header_data[y * width + x];
            vga_memory[320 * y + x] = pixel;
        }
    }

    for(int i = 0; i < 400000000; i++) {
    }

    vga_write_registers(mode_text);
    vga_write_font(font_8x16);
    vga_set_colormap(vga_default_colormap);

    module_init_bootscreen();
}
